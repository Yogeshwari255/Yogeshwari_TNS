package day1.basics;

public class PrimitiveDataTypes {

	public static void main(String[] args) {
		//byte
		byte byteMax=126;
		byte byteMin=-125;
		System.out.println("Min range of byte is"+byteMin+ "Max range of byte is"+ byteMax);
		
		//short
		short shortMax= 32546;
		short shortMin= -32545;
		System.out.println("Minshort range of bytes is"+ shortMin+ "Maxshort range of byte is"+shortMax);
		
		
		//int
		int MaxInt=211162476;
		int MinInt=-2111624786;
		System.out.println("Minint range of byte is"+ MinInt+ "Maxint range of byte is "+ MaxInt);
		
		
		//long
		long MaxLong=789456123698545L;
		long MinLong=-789456123698545L;
		System.out.println("Minlong range of byte is"+ MinLong+ "MaxLong range of byte is"+MaxLong);
		
		
		//float
		float MaxFloat= 5672.1258964f;
		float MinFloat=-5672.1258964f;
		System.out.println("MinFloat range of byte is"+ MinFloat+"MaxFloat range of byte is"+ MaxFloat); 
		
		
		//double
		double d= 5623.589647f;
		System.out.println("double value is"+d);
		
		//boolean
		boolean cat=true;
		System.out.println("boolean value is "+cat);
	
		
		
		
		
		
		
		
		
		

	}

}
 