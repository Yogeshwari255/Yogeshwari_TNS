package day1.basics;

public class CharProgram {

	public static void main(String[] args) {
		char ch = 'y';
		System.out.println(ch);

		char ch1 = 25;
		System.out.println(ch1);

		char var1 = '\u00B6';
		System.out.println(var1);
		
		char var2 = '\u20BC';
		System.out.println(var2);

		int a = 'B';
		System.out.println(a);

	}

}
